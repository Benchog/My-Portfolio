import fs from 'fs';
import path from 'path';

const ROOT = process.cwd();
const OUT_JSON = path.join(ROOT, 'gallery.manifest.json');
const OUT_JS = path.join(ROOT, 'gallery.manifest.js');
const OVERRIDES_PATH = path.join(ROOT, 'gallery.overrides.json');

const exts = new Set(['.png', '.jpg', '.jpeg', '.webp', '.gif', '.svg']);

function isImageFile(name) {
  const ext = path.extname(name).toLowerCase();
  return exts.has(ext);
}

function shouldExclude(name) {
  const n = name.toLowerCase();
  if (n.startsWith('ims-')) return true;
  if (n.startsWith('sp-')) return true;
  if (n.startsWith('primedraft-')) return true;
  if (n.startsWith('benchtech-')) return true;
  if (n.includes('furniture_sales_dashboard')) return true;
  if (n.includes('marketing_performance_dashboard')) return true;
  if (n === 'dashboard.png') return true;
  if (n === 'money.png') return true;
  if (n === 'coach.png') return true;
  if (n === 'reports.png') return true;
  if (n === 'arena.png') return true;
  if (n === 'about.png') return true;
  if (n === '1750806398142.jpeg') return true;
  return false;
}

function inferCategory(name) {
  const raw = (name || '').trim();
  const n = raw.toLowerCase();
  const base = raw.split(/[/\\]/).pop().toLowerCase();

  const designComposites = new Set([
    'aj 1.png',
    'aj 3-.png',
    'aj ofori 2-.png',
    'kof-01.png',
    'off-01.png',
    'patience_business.png'
  ]);
  if (designComposites.has(base)) return 'design';

  if (n.includes('wmremove')) return 'photography';
  if (n.startsWith('file_')) return 'photography';
  if (n.startsWith('psx_')) {
    if (n.includes('065920') || n.includes('060905') || n.includes('164557')) return 'design';
    return 'photography';
  }

  if (
    n.includes('poster') ||
    n.includes('welcome') ||
    n.includes('nominations') ||
    n.includes('committee') ||
    n.includes('vetting') ||
    n.includes('design') ||
    n.includes('product') ||
    n.includes('social media') ||
    n.includes('web')
  ) return 'design';

  return 'design';
}

function titleFromFilename(name) {
  return name
    .replace(/\.[^.]+$/, '')
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function loadOverrides() {
  try {
    const raw = fs.readFileSync(OVERRIDES_PATH, 'utf8');
    const data = JSON.parse(raw);
    if (!data || typeof data !== 'object') return {};
    return data;
  } catch {
    return {};
  }
}

const overrides = loadOverrides();

const files = fs.readdirSync(ROOT, { withFileTypes: true })
  .filter(d => d.isFile())
  .map(d => d.name)
  .filter(isImageFile)
  .filter(f => !shouldExclude(f))
  .sort((a, b) => a.localeCompare(b));

const items = files.map(file => {
  const o = overrides[file];
  let category = inferCategory(file);
  if (o === 'photography' || o === 'design') category = o;
  return {
    file,
    category,
    title: titleFromFilename(file),
  };
});

const payload = { generatedAt: new Date().toISOString(), items };

fs.writeFileSync(OUT_JSON, JSON.stringify(payload, null, 2), 'utf8');

const js = `/* Auto-generated by generate-gallery-manifest.mjs — do not edit by hand */
window.__GALLERY_MANIFEST__ = ${JSON.stringify(payload)};
`;
fs.writeFileSync(OUT_JS, js, 'utf8');

console.log(`Wrote ${items.length} items -> ${path.basename(OUT_JSON)} + ${path.basename(OUT_JS)}`);
